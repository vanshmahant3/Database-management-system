CREATE OR REPLACE PROCEDURE transfer_funds (
    p_from_acc IN NUMBER,
    p_to_acc   IN NUMBER,
    p_amount   IN NUMBER
) 
IS
    v_balance NUMBER;
    v_txn_id  NUMBER;
BEGIN
    IF p_from_acc = p_to_acc THEN
        RAISE_APPLICATION_ERROR(-20004, 'Sender and receiver cannot be the same.');
    END IF;

    IF p_amount > 10000 THEN
        RAISE_APPLICATION_ERROR(-20005, 'Transfer amount exceeds the limit of ₹10,000.');
    END IF;

    SELECT balance INTO v_balance 
    FROM accounts 
    WHERE account_no = p_from_acc;

    IF v_balance < p_amount THEN
        RAISE_APPLICATION_ERROR(-20001, 'Insufficient balance.');
    END IF;
    UPDATE accounts SET balance = balance - p_amount WHERE account_no = p_from_acc;
    UPDATE accounts SET balance = balance + p_amount WHERE account_no = p_to_acc;
    SELECT NVL(MAX(transaction_id), 0) + 1 INTO v_txn_id FROM transactions;

    INSERT INTO transactions (transaction_id, from_account, to_account, amount, transaction_date)
    VALUES (v_txn_id, p_from_acc, p_to_acc, p_amount, SYSDATE);

    COMMIT;

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20002, 'Invalid account number.');
    WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
END;
/


CREATE OR REPLACE PROCEDURE display_monthly_txns (
    p_acc_no NUMBER,
    p_month_year VARCHAR2 
) AS
BEGIN
    FOR rec IN (
        SELECT * FROM transactions 
        WHERE (from_account = p_acc_no OR to_account = p_acc_no)
        AND TO_CHAR(transaction_date, 'MM-YYYY') = p_month_year
    )
    LOOP
        DBMS_OUTPUT.PUT_LINE(
            'Transaction ID: ' || rec.transaction_id || ' | ' ||
            'Sender: '         || rec.from_account    || ' | ' ||
            'Receiver: '       || rec.to_account      || ' | ' ||
            'Amount: '         || rec.amount          || ' | ' ||
            'Date: '           || rec.transaction_date
        );
    END LOOP;
END;
/

