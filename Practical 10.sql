    v_balance    accounts.balance%TYPE;
BEGIN
    SELECT balance INTO v_balance FROM accounts WHERE account_no = v_account_no;
    
    DBMS_OUTPUT.PUT_LINE('Account Number: ' || v_account_no);
    DBMS_OUTPUT.PUT_LINE('Available Balance: ' || v_balance);
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('Account not found');
END;
/

DECLARE
    v_account_no NUMBER := &account_no;
BEGIN
    FOR rec IN (
        SELECT * FROM transactions 
        WHERE from_account = v_account_no OR to_account = v_account_no
    )
    LOOP
        DBMS_OUTPUT.PUT_LINE(
            rec.transaction_id || ' ' || 
            rec.from_account || ' ' || 
            rec.to_account || ' ' || 
            rec.amount || ' ' || 
            rec.transaction_date
        );
    END LOOP;
END;
/

CREATE OR REPLACE FUNCTION get_balance(p_acc_no NUMBER) 
RETURN NUMBER 
AS
    v_balance NUMBER;
BEGIN
    SELECT balance 
    INTO v_balance 
    FROM accounts 
    WHERE account_no = p_acc_no;

    RETURN v_balance;

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RETURN -1; 
END;
/

